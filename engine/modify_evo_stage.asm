ModifyEvoStage:
	push af
	push bc
	push de
	push hl
	ld a, [wcf91]
	ld b, 0
	ld c, a
	ld d, 20
	ld hl, IsTwoStageMon
	add hl, bc
	ld a, [hl]
	and a 
	jr z, .notTwoStage
	ld d, 25
.notTwoStage		
	ld a, [wCurEnemyLVL]
	cp d
	jr nc, .isNotFirstStage
	ld hl, Stage3ToStage2
	call ApplyStageChange
	ld hl, Stage2ToStage1
	call ApplyStageChange
	jr .evoScalingDone
.isNotFirstStage
	ld a, [wCurEnemyLVL]
	cp 35
	jr nc, .higherThan35
	ld hl, Stage3ToStage2
	call ApplyStageChange
	ld hl, Stage1ToStage2
	call ApplyStageChange
	jr .evoScalingDone
.higherThan35		
	ld hl, Stage1ToStage2
	call ApplyStageChange
	ld hl, Stage2ToStage3
	call ApplyStageChange
.evoScalingDone	
	ld a, c 
	ld [wcf91], a
	pop hl
	pop de
	pop bc
	pop af

	ret

ApplyStageChange:
	add hl, bc
	ld a, [hl]
	cp 0
	jr z, .done
	ld c, a
.done		
	ret
	
